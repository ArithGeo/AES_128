// operating mode: ECB & CBC
// no padding requires len % 16 == 0
// padding rule: PKCS#7

// ECB encryption
static int aes128EcbEncWithoutPadding(uint8_t* cipher, const uint8_t* plain, const uint8_t* roundKey, int len){
    for(int i = 0; i < len; i += 16){
        aes128BlockEnc(&cipher[i], &plain[i], roundKey);
    }
    return len;
}

static int aes128EcbEncWithPadding(uint8_t* cipher, const uint8_t* plain, const uint8_t* roundKey, int len){
    int remained = len % 16;
    int padNum = 16 - remained;
    int lenTrunc = len / 16 * 16;
    
    for(int i = 0; i < lenTrunc; i += 16){
        aes128BlockEnc(&cipher[i], &plain[i], roundKey);
    }

    uint8_t buffer[16];
    for(int i = 0; i < remained; i++){
        buffer[i] = plain[lenTrunc + i];
    }
    for(int i = 16 - padNum; i < 16; i++){
        buffer[i] = padNum;
    }
    aes128BlockEnc(&cipher[lenTrunc], buffer, roundKey);

    return lenTrunc + 16;
}

int aes128EcbEnc(uint8_t* cipher, const uint8_t* plain, const uint8_t* roundKey, int len, int padding){
    if(padding){
        return aes128EcbEncWithPadding(cipher, plain, roundKey, len);
    } else {
        return aes128EcbEncWithoutPadding(cipher, plain, roundKey, len);
    }
}

// ECB decryption
static int aes128EcbDecWithoutPadding(uint8_t* plain, const uint8_t* cipher, const uint8_t* roundKey, int len){
    for(int i = 0; i < len; i += 16){
        aes128BlockDec(&plain[i], &cipher[i], roundKey);
    }
    return len;
}

static int aes128EcbDecWithPadding(uint8_t* plain, const uint8_t* cipher, const uint8_t* roundKey, int len){
    for(int i = 0; i < len; i += 16){
        aes128BlockDec(&plain[i], &cipher[i], roundKey);
    }
    int padNum = plain[len - 1];
    return len - padNum;
}

int aes128EcbDec(uint8_t* cipher, const uint8_t* plain, const uint8_t* roundKey, int len, int padding){
    if(padding){
        return aes128EcbDecWithPadding(cipher, plain, roundKey, len);
    } else {
        return aes128EcbDecWithoutPadding(cipher, plain, roundKey, len);
    }
}

// CBC encryption
static int aes128CbcEncWithoutPadding(uint8_t* cipher, const uint8_t* plain, const uint8_t* roundKey, int len, const uint8_t* initVect){
    uint8_t buffer[16];

    if(len >= 16){
        for(int i = 0; i < 16; i++){
            buffer[i] = plain[i] ^ initVect[i];
        }
        aes128BlockEnc(&cipher[0], buffer, roundKey);

        for(int i = 16; i < len; i += 16){
            for(int j = 0; j < 16; j++){
                buffer[j] = plain[i + j] ^ cipher[i + j - 16];
            }
            aes128BlockEnc(&cipher[i], buffer, roundKey);
        }

        return len;
    } else {
        return 0;
    }
}

static int aes128CbcEncWithPadding(uint8_t* cipher, const uint8_t* plain, const uint8_t* roundKey, int len, const uint8_t* initVect){
    int remained = len % 16;
    int padNum = 16 - remained;
    int lenTrunc = len / 16 * 16;

    uint8_t buffer[16];

    if(len >= 16){
        for(int i = 0; i < 16; i++){
            buffer[i] = plain[i] ^ initVect[i];
        }
        aes128BlockEnc(&cipher[0], buffer, roundKey);

        for(int i = 16; i < lenTrunc; i += 16){
            for(int j = 0; j < 16; j++){
                buffer[j] = plain[i + j] ^ cipher[i + j - 16];
            }
            aes128BlockEnc(&cipher[i], buffer, roundKey);
        }

        for(int i = 0; i < remained; i++){
            buffer[i] = plain[lenTrunc + i] ^ cipher[lenTrunc + i - 16];
        }
        for(int i = 16 - padNum; i < 16; i++){
            buffer[i] = padNum ^ cipher[lenTrunc + i - 16];
        }
        aes128BlockEnc(&cipher[lenTrunc], buffer, roundKey);
    } else {
        for(int i = 0; i < remained; i++){
            buffer[i] = plain[i] ^ initVect[i];
        }
        for(int i = 16 - padNum; i < 16; i++){
            buffer[i] = padNum ^ initVect[i];
        }
        aes128BlockEnc(&cipher[0], buffer, roundKey);
    }

    return lenTrunc + 16;
}

int aes128CbcEnc(uint8_t* cipher, const uint8_t* plain, const uint8_t* roundKey, int len, const uint8_t* initVect, int padding){
    if(padding){
        return aes128CbcEncWithPadding(cipher, plain, roundKey, len, initVect);
    } else {
        return aes128CbcEncWithoutPadding(cipher, plain, roundKey, len, initVect);
    }
}

// CBC Decryption
static int aes128CbcDecWithoutPadding(uint8_t* plain, const uint8_t* cipher, const uint8_t* roundKey, int len, const uint8_t* initVect){
    if(len >= 16){
        aes128BlockDec(&plain[0], &cipher[0], roundKey);
        for(int i = 0; i < 16; i++){
            plain[i] ^= initVect[i];
        }

        for(int i = 16; i < len; i += 16){
            aes128BlockDec(&plain[i], &cipher[i], roundKey);
            for(int j = 0; j < 16; j++){
                plain[i + j] ^= cipher[i + j - 16];
            }
        }

        return len;
    } else {
        return 0;
    }
}

static int aes128CbcDecWithPadding(uint8_t* plain, const uint8_t* cipher, const uint8_t* roundKey, int len, const uint8_t* initVect){
    if(len >= 16){
        aes128BlockDec(&plain[0], &cipher[0], roundKey);
        for(int i = 0; i < 16; i++){
            plain[i] ^= initVect[i];
        }

        for(int i = 16; i < len; i += 16){
            aes128BlockDec(&plain[i], &cipher[i], roundKey);
            for(int j = 0; j < 16; j++){
                plain[i + j] ^= cipher[i + j - 16];
            }
        }

        int padNum = plain[len - 1];
        return len - padNum;
    } else {
        return 0;
    }
}

int aes128CbcDec(uint8_t* cipher, const uint8_t* plain, const uint8_t* roundKey, int len, const uint8_t* initVect, int padding){
    if(padding){
        return aes128CbcDecWithPadding(cipher, plain, roundKey, len, initVect);
    } else {
        return aes128CbcDecWithoutPadding(cipher, plain, roundKey, len, initVect);
    }
}


