// multiplication on GF(2^8)
static uint8_t gfMult(uint16_t a, uint16_t b){
    const uint16_t gf_size  = 0x0100;
    const uint16_t irr_poly = 0x011B;

    uint16_t ret = 0;

    for(int k = 0; k < 8; k++){
        if((b >> k) & 1) ret ^= a;
        a <<= 1;
        if(a & gf_size) a ^= irr_poly;
    }

    return ret;
}

static void byteSub(uint8_t* dest, const uint8_t* source){
    for(int i = 0; i < 16; i++){
        dest[i] = sBox[source[i]];
    }
}

static void byteSubInv(uint8_t* dest, const uint8_t* source){
    for(int i = 0; i < 16; i++){
        dest[i] = sBoxInv[source[i]];
    }
}

static void shiftRow(uint8_t* dest, const uint8_t* source){
    // 0th row
    dest[ 0] = source[ 0];
    dest[ 4] = source[ 4];
    dest[ 8] = source[ 8];
    dest[12] = source[12];

    // 1st row
    dest[ 1] = source[ 5];
    dest[ 5] = source[ 9];
    dest[ 9] = source[13];
    dest[13] = source[ 1];

    // 2nd row
    dest[ 2] = source[10];
    dest[ 6] = source[14];
    dest[10] = source[ 2];
    dest[14] = source[ 6];

    // 3rd row
    dest[ 3] = source[15];
    dest[ 7] = source[ 3];
    dest[11] = source[ 7];
    dest[15] = source[11];
}

static void shiftRowInv(uint8_t* dest, const uint8_t* source){
    // 0th row
    dest[ 0] = source[ 0];
    dest[ 4] = source[ 4];
    dest[ 8] = source[ 8];
    dest[12] = source[12];

    // 1st row
    dest[ 5] = source[ 1];
    dest[ 9] = source[ 5];
    dest[13] = source[ 9];
    dest[ 1] = source[13];

    // 2nd row
    dest[10] = source[ 2];
    dest[14] = source[ 6];
    dest[ 2] = source[10];
    dest[ 6] = source[14];

    // 3rd row
    dest[15] = source[ 3];
    dest[ 3] = source[ 7];
    dest[ 7] = source[11];
    dest[11] = source[15];
}

static void mixColumn(uint8_t* dest, const uint8_t* source){
    for(int i = 0; i < 16; i++){
        dest[i] = 0;
    }

    for(int j = 0; j < 4; j++){
        for(int k = 0; k < 4; k++){
            for(int i = 0; i < 4; i++){
                dest[i + 4 * j] ^= gfMult(mixColumnMat[i + 4 * k], source[k + 4 * j]);
            }
        }
    }
}

static void mixColumnInv(uint8_t* dest, const uint8_t* source){
    for(int i = 0; i < 16; i++){
        dest[i] = 0;
    }

    for(int j = 0; j < 4; j++){
        for(int k = 0; k < 4; k++){
            for(int i = 0; i < 4; i++){
                dest[i + 4 * j] ^= gfMult(mixColumnMatInv[i + 4 * k], source[k + 4 * j]);
            }
        }
    }
}

void expandKey(uint8_t* roundKey, const uint8_t* key){
    for(int i = 0; i < 16; i++){
        roundKey[i] = key[i];
    }
    
    for(int i = 4; i < 44; i++){
        if(i % 4 != 0){
            for(int j = 0; j < 4; j++){
                roundKey[4 * i + j] = roundKey[4 * (i - 4) + j] ^ roundKey[4 * (i - 1) + j];
            }
        } else {
            roundKey[4 * i + 0] = roundKey[4 * (i - 4) + 0] ^ sBox[roundKey[4 * (i - 1) + 1]] ^ roundConst[i / 4 - 1];
            roundKey[4 * i + 1] = roundKey[4 * (i - 4) + 1] ^ sBox[roundKey[4 * (i - 1) + 2]];
            roundKey[4 * i + 2] = roundKey[4 * (i - 4) + 2] ^ sBox[roundKey[4 * (i - 1) + 3]];
            roundKey[4 * i + 3] = roundKey[4 * (i - 4) + 3] ^ sBox[roundKey[4 * (i - 1) + 0]];
        }
    }
}

static void addRoundKey(uint8_t* dest, const uint8_t* source, const uint8_t* roundKey, int round){ 
    for(int i = 0; i < 16; i++){
        dest[i] = source[i] ^ roundKey[round * 16 + i];
    }
}

static void addRoundKeyInv(uint8_t* dest, const uint8_t* source, const uint8_t* roundKey, int round){
    addRoundKey(dest, source, roundKey, round); // self-inverse
}

// AES-128: 16B -> 16B encryption
void aes128BlockEnc(uint8_t* dest, const uint8_t* source, const uint8_t* roundKey){
    uint8_t buffer0[16];
    uint8_t buffer1[16];

    addRoundKey(buffer0, source, roundKey, 0);
    for(int round = 1; round <= 9; round++){
        byteSub(buffer1, buffer0);
        shiftRow(buffer0, buffer1);
        mixColumn(buffer1, buffer0);
        addRoundKey(buffer0, buffer1, roundKey, round);
    }

    // round 10 has no mixColumn step
    byteSub(buffer1, buffer0);
    shiftRow(buffer0, buffer1);
    addRoundKey(dest, buffer0, roundKey, 10);
}

// AES-128: 16B -> 16B decryption
void aes128BlockDec(uint8_t* dest, const uint8_t* source, const uint8_t* roundKey){
    uint8_t buffer0[16];
    uint8_t buffer1[16];

    // round 10 has no mixColumn step
    addRoundKeyInv(buffer0, source, roundKey, 10);
    shiftRowInv(buffer1, buffer0);
    byteSubInv(buffer0, buffer1);

    for(int round = 9; round >= 1; round--){
        addRoundKeyInv(buffer1, buffer0, roundKey, round);
        mixColumnInv(buffer0, buffer1);
        shiftRowInv(buffer1, buffer0);
        byteSubInv(buffer0, buffer1);
    }
    addRoundKeyInv(dest, buffer0, roundKey, 0);
}



